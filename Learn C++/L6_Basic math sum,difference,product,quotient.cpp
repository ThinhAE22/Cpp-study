//How to do basic math in C++, Part 2
#include<iostream>

using namespace std;

int main() {
    cout << "2468 + 1234 = " << 2468 + 1234 << endl;
	cout << "2468 - 1234 = " << 2468 - 1234 << endl;
	cout << "2468 * 1234 = " << 2468 * 1234 << endl;
	cout << "2468 / 1234 = " << 2468 / 1234;
	return 0;
}